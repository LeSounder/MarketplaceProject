using Marketplace.Service.Interfaces;
using MarketplaceProject.DAL.Interfaces;
using MarketplaceProject.Domain.Entities;
using MarketplaceProject.Domain.Enum;
using MarketplaceProject.Domain.Response;
using MarketplaceProject.Domain.ViewModels.Account;
using MarketplaceProject.Domain.Hash;
using Microsoft.Extensions.Logging;
using System.Security.Claims;

namespace Marketplace.Service.Implementations
{
    public class AccountService : IAccountService
    {
        private readonly IBaseRepository<User> _userRepository;
        private readonly ILogger<AccountService> _logger;

        public AccountService(IBaseRepository<User> userRepository,
            ILogger<AccountService> logger)
        {
            _userRepository = userRepository;
            _logger = logger;
        }

        public BaseResponse<ClaimsIdentity> Register(RegisterViewModel registerViewModel)
        {
            try
            {
                var user = _userRepository.Select().FirstOrDefault(x => x.UserName == registerViewModel.UserName);
                if (user != null)
                {
                    return new BaseResponse<ClaimsIdentity>()
                    {
                        Description = "Пользователь с таким логином уже есть",
                    };
                }

                user = new User()
                {
                    UserName = registerViewModel.UserName,
                    Role = UserRole.User,
                    Password = HashPassword.HashPass(registerViewModel.Password),
                };

                _userRepository.Create(user);

              

                
                var result = Authenticate(user);

                return new BaseResponse<ClaimsIdentity>()
                {
                    Data = result,
                    Description = "Объект добавился",
                    StatusCode = StatusCode.OK
                };
            }
            catch (Exception ex)
            {
                _logger.LogError(ex, $"[Register]: {ex.Message}");
                return new BaseResponse<ClaimsIdentity>()
                {
                    Description = ex.Message,
                    StatusCode = StatusCode.InternalServerError
                };
            }
        }

        public BaseResponse<ClaimsIdentity> Login(LoginViewModel loginViewModel)
        {
            try
            {
                var user = _userRepository.Select().FirstOrDefault(x => x.UserName == loginViewModel.UserName);
                if (user == null)
                {
                    return new BaseResponse<ClaimsIdentity>()
                    {
                        Description = "Пользователь не найден"
                    };
                }

                if (user.Password != HashPassword.HashPass(loginViewModel.Password))
                {
                    return new BaseResponse<ClaimsIdentity>()
                    {
                        Description = "Неверный пароль или логин"
                    };
                }
                var result = Authenticate(user);

                return new BaseResponse<ClaimsIdentity>()
                {
                    Data = result,
                    StatusCode = StatusCode.OK
                };
            }
            catch (Exception ex)
            {
                _logger.LogError(ex, $"[Login]: {ex.Message}");
                return new BaseResponse<ClaimsIdentity>()
                {
                    Description = ex.Message,
                    StatusCode = StatusCode.InternalServerError
                };
            }
        }


        private ClaimsIdentity Authenticate(User user)
        {
            var claims = new List<Claim>
            {
                new Claim(ClaimsIdentity.DefaultNameClaimType, user.UserName),
                new Claim(ClaimsIdentity.DefaultRoleClaimType, user.Role.ToString())
            };
            
            return new ClaimsIdentity(claims, "ApplicationCookie", ClaimsIdentity.DefaultNameClaimType, ClaimsIdentity.DefaultRoleClaimType);
        }
    }
}
